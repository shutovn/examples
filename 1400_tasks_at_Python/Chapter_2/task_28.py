#else:
#    e = ( b - a ) / 2
#h = e * math.tan(alphar)
#S = ( a + b ) / 2 * h

if a > b:
    S = (a**2-b**2)/4*math.tan(alphar)
else:
    S = (b**2-a**2)/4*math.tan(alphar)

print ("Площадь искомой трапеции равна " + str(S))

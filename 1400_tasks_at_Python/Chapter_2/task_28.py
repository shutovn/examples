# Даны основания равнобедренной трапеции и угол при большем основании. Найти площадь трапеции.

import math
# вводим известные значения
# первое основание
a = float(input("Введите значение первого основания: "))
# второе основание
b = float(input("Введите значение второго основания: "))
# Угол в градусах при большем основание
alpha = float(input("Введите значение угла в градусах при большем основание: "))

# переводим градусы в радианы
alphar = alpha * math.pi / 180

# вычисляем
#if a > b:

n = math.ceil( s / 120)

x = s - (int( s / 120 ) * 120)
y = math.ceil( x / 15 )

n_b = s - (int( s / 10 ) * 10)
y_b = math.ceil( s / 150 )

print("1-ый вариант: "+str(tier[n]) +str(", ")+str(section[y]))
print("2-ой вариант: "+str(tier[n_b]) +str(", ")+str(section[y_b]))

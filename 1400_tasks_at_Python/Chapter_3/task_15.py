import math
s = int(input("Введите номер места в стеллаже: "))

tier = ["Ярус-10","Ярус-1","Ярус-2","Ярус-3","Ярус-4","Ярус-5","Ярус-6","Ярус-7","Ярус-8","Ярус-9","Ярус-10",]
